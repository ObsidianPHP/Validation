namespace Obsidian\Validation\Rules;

use Obsidian\Validation\RuleInterface;
use Obsidian\Validation\Validator;

/**
 * Name: `different`
 *
 * This rule ensures a specific field is different to another existing field. Usage: `different:FIELD_NAME`
 */
class Different implements RuleInterface {
    /**
     * {@inheritdoc}
     * @return bool|string|array
     */
    function validate($value, string $key, array $fields, $options, bool $exists, Validator $validator) {
        if(!$exists) {
            return false;
        }
        
        if($value === $fields[$options]) {
            return array('formvalidator_make_different', array('{0}' => $options));
        }
        
        return true;
    }
}
