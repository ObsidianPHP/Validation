namespace Obsidian\Validation\Rules;

use Obsidian\Validation\RuleInterface;
use Obsidian\Validation\Validator;

/**
 * Name: `lowercase`
 *
 * This rule ensures a specific field is all lowercase.
 */
class Lowercase implements RuleInterface {
    /**
     * {@inheritdoc}
     * @return bool|string|array
     */
    function validate($value, string $key, array $fields, $options, bool $exists, Validator $validator) {
        if(!$exists) {
            return false;
        }
        
        if(!\is_string($value) || \mb_strtolower($value) !== $value) {
            return 'formvalidator_make_lowercase';
        }
        
        return true;
    }
}
